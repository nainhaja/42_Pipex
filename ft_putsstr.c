#include "pipex.h"

void	ft_putchar(int c)
{
	write(2, &c, 1);
}

void	ft_putsstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}
